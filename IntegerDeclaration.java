public class IntegerDeclaration {
    public static void main(String [] args){
        int a = 5;
        int b = 7;
        int c = 9;
        System.out.println("First integer, a = " + a);
        System.out.println("Second integer, b = " + b);
        System.out.println("Third integer, c = " + c);
    }
}