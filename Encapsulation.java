public class Encapsulation {

    
    private String name;
    private  Double dob;

    public String getName() {
        return name;

    }

    public void setName(String name){
        this.name = name;
    }

    public Double getDob() {

        return dob;
    }
    public void setDob(Double dob){
        this.dob = dob;
    }


}